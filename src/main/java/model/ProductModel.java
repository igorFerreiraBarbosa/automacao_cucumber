package model;

public class ProductModel {
	private int id_massas;
	private String nameProduct;
	private String customization;
	private String display;
	private String displayResolution;
	private String displaySize;
	private String productMemory;
	private String operatingSystem;
	private String processor;
	private String touchscreen;
	private String weight;
	private String color;
	
	
	public int getId_massas() {
		return id_massas;
	}
	public void setId_massas(int id_massas) {
		this.id_massas = id_massas;
	}
	
	public String getNameProduct() {
		return nameProduct;
	}
	public void setNameProduct(String nameProduct) {
		this.nameProduct = nameProduct;
	}
	
	public String getCustomization() {
		return customization;
	}
	
	public void setCustomization(String customization) {
		this.customization = customization;
	}
	
	public String getDisplay() {
		return display;
	}
	
	public void setDisplay(String display) {
		this.display = display;
	}
	
	public String getDisplayResolution() {
		return displayResolution;
	}
	
	public void setDisplayResolution(String displayResolution) {
		this.displayResolution = displayResolution;
	}
	
	public String getDisplaySize() {
		return displaySize;
	}
	
	public void setDisplaySize(String displaySize) {
		this.displaySize = displaySize;
	}
	
	public String getProductMemory() {
		return productMemory;
	}
	
	public void setProductMemory(String productMemory) {
		this.productMemory = productMemory;
	}
	
	public String getOperatingSystem() {
		return operatingSystem;
	}
	
	public void setOperatingSystem(String operatingSystem) {
		this.operatingSystem = operatingSystem;
	}
	
	public String getProcessor() {
		return processor;
	}
	
	public void setProcessor(String processor) {
		this.processor = processor;
	}
	
	public String getTouchscreen() {
		return touchscreen;
	}
	
	public void setTouchscreen(String touchscreen) {
		this.touchscreen = touchscreen;
	}
	
	public String getWeight() {
		return weight;
	}
	
	public void setWeight(String weight) {
		this.weight = weight;
	}
	
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	
	
}
